PaidUsers AS (
    SELECT DISTINCT t.UserId
    FROM dbo.Transactions t WITH (NOLOCK)
    INNER JOIN MatchedUsers m ON m.UserId = t.UserId
    WHERE t.Status = 'Complete'
      AND t.PaymentSystemId <> 'WebAdmin'
