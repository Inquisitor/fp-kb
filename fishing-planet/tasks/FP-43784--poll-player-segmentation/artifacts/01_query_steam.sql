LEFT JOIN dbo.Profiles pr WITH (NOLOCK) ON pr.UserId = mu.UserId
LEFT JOIN dbo.UserCountries uc WITH (NOLOCK) ON uc.UserId = mu.UserId
LEFT JOIN PaidUsers pu ON pu.UserId = mu.UserId
